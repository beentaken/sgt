int rocket_make_player_images(void)
{
    int player, angle, x, y, ic;
    float rx, ry, rz, ccx, ccy, ccz, sp, cc, ss, ridge;

    for (angle = 0; angle < 32; angle++) {
	cc = cos(angle * PI / 16);
	ss = sin(angle * PI / 16);
	rocket_player_images[0][angle] = makeimage(17, 17, 8, 8);
	rocket_player_images[1][angle] = makeimage(17, 17, 8, 8);
	for (x = -8; x <= +8; x++)
	    for (y = -8; y <= +8; y++) {
		rx = x * cc - y * ss;
		ry = y * cc + x * ss;
		if (ry > 3 || ry < -8) {
		    rz = -1;
		} else {
		    rz = sqr((ry+9)/3) - sqr(rx);
		    if (rz < 0)
			rz = -1;
		    else
			rz = sqrt(rz);
		    if (rz == 0)
			ridge = (rx > 0 ? PI / 2 : -PI / 2);
		    else
			ridge = atan(rx/rz);
		    ridge = 1.5 * sin(ry*2 + ridge*2);
		    ccx = 0;
		    ccy = (10 + ridge) * ry/9 + 1 + ridge;
		    ccz = 0;
		}
		if (rz < 0)
		    ic = 255;
		else if (ry <= -6)
		    ic = 140;
		else {
		    sp = (ccx-rx) * (cc-ss) + (ccy-ry) * (cc+ss) + (rz-ccz);
		    sp /= sqrt(3*(sqr(ccx-rx)+sqr(ccy-ry)+sqr(ccz-rz)));
		    ic = (int)(0.5 + (sp*31.5+31.5))+64;
		}
		imagepixel(rocket_player_images[0][angle], x, y, ic);
		if (ic == 140)
		    ic = 141;
		imagepixel(rocket_player_images[1][angle], x, y, ic);
	    }
    }
}

